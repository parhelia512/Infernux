import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const docsRoot = path.resolve("docs");
const sourceFile = path.join(docsRoot, "tools", "i18n-source.json");
const check = process.argv.includes("--check");
const pageConfigs = {
    "404": [],
    index: [],
    wiki: ["wiki.js", "docs-health.js", "docs-recent.js"],
    roadmap: [],
    community: ["community.js"],
    "community-topic": ["community-topic.js"],
    download: ["download.js", "pwa-install.js"],
};

function stableDictionary(keys, dictionary) {
    return Object.fromEntries([...keys].sort().map((key) => [key, dictionary[key]]));
}

function serialized(value) {
    return JSON.stringify(value, null, 4);
}

function runtimeSource(common) {
    return `/* Generated by docs/tools/build-i18n.mjs. Edit docs/tools/i18n-source.json instead. */
const pageTranslations = globalThis.INFERNUX_PAGE_TRANSLATIONS || { en: {}, zh: {} };
delete globalThis.INFERNUX_PAGE_TRANSLATIONS;

const translations = {
    en: { ...${serialized(common.en)}, ...(pageTranslations.en || {}) },
    zh: { ...${serialized(common.zh)}, ...(pageTranslations.zh || {}) }
};

function requestedPageLanguage() {
    try {
        if (typeof window === 'undefined') return null;
        const requested = new URLSearchParams(window.location.search).get('lang');
        if (requested === 'zh' || requested === 'zh-CN') return 'zh';
        if (requested === 'en') return 'en';
    } catch {}
    return null;
}

const storedLanguage = localStorage.getItem('lang');
let currentLang = requestedPageLanguage() || (storedLanguage === 'zh' ? 'zh' : 'en');

function translateSiteKey(key, lang = currentLang) {
    return translations[lang]?.[key] || "";
}

function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        const value = translations[lang]?.[key];
        if (!value) {
            return;
        }
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = value;
        } else {
            element.innerHTML = value;
        }
    });
    document.querySelectorAll('[data-href-en][data-href-zh]').forEach((element) => {
        element.setAttribute('href', lang === 'zh'
            ? element.getAttribute('data-href-zh')
            : element.getAttribute('data-href-en'));
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach((element) => {
        const key = element.getAttribute('data-i18n-aria-label');
        const value = translations[lang]?.[key];
        if (value) {
            element.setAttribute('aria-label', value);
        }
    });
    const titleKey = document.documentElement.getAttribute('data-title-i18n');
    if (titleKey) {
        const titleText = translations[lang]?.[titleKey];
        if (titleText) {
            document.title = titleText;
        }
    }
    const langText = document.getElementById('lang-text');
    if (langText) {
        langText.textContent = lang === 'en' ? '中文' : 'EN';
    }
    document.documentElement.lang = lang === 'en' ? 'en' : 'zh-CN';
    document.dispatchEvent(new CustomEvent('site:language-changed', { detail: { lang } }));
}

function toggleLanguage() {
    applyLanguage(currentLang === 'en' ? 'zh' : 'en');
}

document.addEventListener('DOMContentLoaded', function() {
    applyLanguage(currentLang);
});
`;
}

function pageSource(page, translations) {
    return `/* Generated by docs/tools/build-i18n.mjs. Edit docs/tools/i18n-source.json instead. */
globalThis.INFERNUX_PAGE_TRANSLATIONS = ${serialized(translations)};
`;
}

async function pageUsage(page, scripts, keys) {
    let source = await readFile(path.join(docsRoot, `${page}.html`), "utf8");
    for (const script of scripts) source += `\n${await readFile(path.join(docsRoot, "js", script), "utf8")}`;
    return new Set(keys.filter((key) => source.includes(`"${key}"`) || source.includes(`'${key}'`)));
}

async function output(file, expected) {
    if (check) {
        let actual = null;
        try {
            actual = await readFile(file, "utf8");
        } catch (error) {
            if (error.code !== "ENOENT") throw error;
        }
        if (actual !== expected) throw new Error(`${path.relative(process.cwd(), file)} is stale; run node docs/tools/build-i18n.mjs.`);
        return;
    }
    await writeFile(file, expected, "utf8");
}

const source = JSON.parse(await readFile(sourceFile, "utf8"));
const languages = ["en", "zh"];
for (const language of languages) {
    if (!source[language] || typeof source[language] !== "object" || Array.isArray(source[language])) {
        throw new Error(`${path.relative(process.cwd(), sourceFile)}: '${language}' must be an object.`);
    }
}
const sourceKeys = Object.keys(source.en).sort();
const translatedKeys = Object.keys(source.zh).sort();
if (JSON.stringify(sourceKeys) !== JSON.stringify(translatedKeys)) {
    throw new Error("English and Chinese localization keys must match exactly.");
}
for (const language of languages) {
    for (const key of sourceKeys) {
        if (typeof source[language][key] !== "string" || !source[language][key].trim()) {
            throw new Error(`${language}.${key}: translation must be a non-empty string.`);
        }
    }
}

const usage = new Map();
for (const [page, scripts] of Object.entries(pageConfigs)) {
    usage.set(page, await pageUsage(page, scripts, sourceKeys));
}
const usageCount = new Map(sourceKeys.map((key) => [key, 0]));
for (const keys of usage.values()) {
    for (const key of keys) usageCount.set(key, usageCount.get(key) + 1);
}
const unusedKeys = sourceKeys.filter((key) => usageCount.get(key) === 0);
if (unusedKeys.length) throw new Error(`Unused localization keys: ${unusedKeys.join(", ")}`);

const commonKeys = new Set(sourceKeys.filter((key) => usageCount.get(key) > 1));
const common = Object.fromEntries(languages.map((language) => [language, stableDictionary(commonKeys, source[language])]));
await output(path.join(docsRoot, "js", "i18n.js"), runtimeSource(common));

const routeSizes = [];
for (const [page, usedKeys] of usage) {
    const pageKeys = new Set([...usedKeys].filter((key) => !commonKeys.has(key)));
    const pageTranslations = Object.fromEntries(languages.map((language) => [language, stableDictionary(pageKeys, source[language])]));
    const generated = pageSource(page, pageTranslations);
    await output(path.join(docsRoot, "js", `i18n-${page}.js`), generated);
    routeSizes.push(`${page} ${(Buffer.byteLength(generated) / 1024).toFixed(1)} KiB`);
}

const core = runtimeSource(common);
console.log(`${check ? "Verified" : "Generated"} localized route bundles: core ${(Buffer.byteLength(core) / 1024).toFixed(1)} KiB, ${routeSizes.join(", ")}; ${sourceKeys.length} keys with no unused copy.`);
