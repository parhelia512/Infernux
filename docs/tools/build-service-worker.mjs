import { createHash } from "node:crypto";
import { readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const docsRoot = path.resolve("docs");
const outputFile = path.join(docsRoot, "sw.js");
const check = process.argv.includes("--check");
const maxPrecacheBytes = 2 * 1024 * 1024;

async function namedRoutes(directory, pattern) {
    return (await readdir(path.join(docsRoot, directory), { withFileTypes: true }))
        .filter((entry) => entry.isFile() && pattern.test(entry.name))
        .map((entry) => `/${directory}/${entry.name}`);
}

const hashedTemplateStyles = await namedRoutes("css", /^wiki-template\.[a-f0-9]{16}\.css$/);
if (hashedTemplateStyles.length !== 1) throw new Error(`Expected one content-hashed Wiki template style, found ${hashedTemplateStyles.length}.`);
const hashedWikiCatalogs = await namedRoutes("assets", /^wiki-docs\.[a-f0-9]{16}\.json$/);
if (hashedWikiCatalogs.length !== 1) throw new Error(`Expected one content-hashed Wiki catalog, found ${hashedWikiCatalogs.length}.`);

const routes = [
    "/offline.html",
    "/index.html",
    "/wiki.html",
    "/roadmap.html",
    "/community.html",
    "/download.html",
    "/site.webmanifest",
    "/assets/logo.png",
    "/docs-index.json",
    "/docs-health.json",
    "/learning-paths.json",
    "/api-index.json",
    "/docs-manifest.json",
    "/release.json",
    "/release-notes.json",
    "/llms.txt",
    ...await namedRoutes("css", /\.css$/),
    ...await namedRoutes("js", /\.js$/),
    ...await namedRoutes("assets/fonts", /\.woff2$/),
    ...hashedWikiCatalogs
].filter((route, index, values) => values.indexOf(route) === index).sort();

const evidence = [];
let precacheBytes = 0;
for (const route of routes) {
    const content = await readFile(path.join(docsRoot, route.slice(1)));
    precacheBytes += content.length;
    evidence.push(`${route}\0${createHash("sha256").update(content).digest("hex")}`);
}
if (precacheBytes > maxPrecacheBytes) {
    throw new Error(`PWA precache is ${(precacheBytes / 1024).toFixed(1)} KiB; budget is ${(maxPrecacheBytes / 1024).toFixed(1)} KiB.`);
}

const version = createHash("sha256").update(evidence.join("\n")).digest("hex").slice(0, 16);
const output = `/* Generated by docs/tools/build-service-worker.mjs. Do not edit directly. */
const CACHE_PREFIX = "infernux-pwa-";
const CACHE_VERSION = "${version}";
const CACHE_NAME = CACHE_PREFIX + CACHE_VERSION;
const OFFLINE_URL = "/offline.html";
const PRECACHE_URLS = ${JSON.stringify(routes, null, 2)};

function canCache(response) {
    return response && response.ok && (response.type === "basic" || response.type === "default");
}

async function networkFirst(request, navigationFallback = false) {
    const cache = await caches.open(CACHE_NAME);
    try {
        const response = await fetch(request);
        if (canCache(response)) await cache.put(request, response.clone());
        return response;
    } catch (error) {
        const cached = await cache.match(request, { ignoreSearch: true });
        if (cached) return cached;
        if (navigationFallback) return cache.match(OFFLINE_URL);
        throw error;
    }
}

async function cacheFirst(request) {
    const cache = await caches.open(CACHE_NAME);
    const cached = await cache.match(request, { ignoreSearch: true });
    if (cached) return cached;
    const response = await fetch(request);
    if (canCache(response)) await cache.put(request, response.clone());
    return response;
}

async function staleWhileRevalidate(request) {
    const cache = await caches.open(CACHE_NAME);
    const cached = await cache.match(request, { ignoreSearch: true });
    const refresh = fetch(request).then(async (response) => {
        if (canCache(response)) await cache.put(request, response.clone());
        return response;
    }).catch(() => null);
    if (cached) return cached;
    return await refresh || Response.error();
}

self.addEventListener("install", (event) => {
    event.waitUntil(caches.open(CACHE_NAME).then((cache) => cache.addAll(PRECACHE_URLS)).then(() => self.skipWaiting()));
});

self.addEventListener("activate", (event) => {
    event.waitUntil(caches.keys().then((keys) => Promise.all(
        keys.filter((key) => key.startsWith(CACHE_PREFIX) && key !== CACHE_NAME).map((key) => caches.delete(key))
    )).then(() => self.clients.claim()));
});

self.addEventListener("fetch", (event) => {
    const request = event.request;
    if (request.method !== "GET") return;
    const url = new URL(request.url);
    if (url.origin !== self.location.origin || url.pathname === "/sw.js") return;

    if (request.mode === "navigate") {
        event.respondWith(networkFirst(request, true));
        return;
    }

    if (/\\/css\\/wiki-template\\.[a-f0-9]{16}\\.css$/.test(url.pathname)
        || /\\/assets\\/wiki-docs\\.[a-f0-9]{16}\\.json$/.test(url.pathname)
        || /\\/assets\\/fonts\\//.test(url.pathname)
        || /\\.(?:avif|gif|jpe?g|png|webp|woff2?)$/i.test(url.pathname)) {
        event.respondWith(cacheFirst(request));
        return;
    }

    if (/\\.(?:json|txt)$/i.test(url.pathname)) {
        event.respondWith(networkFirst(request));
        return;
    }

    if (/\\.(?:css|js|webmanifest)$/i.test(url.pathname)) {
        event.respondWith(staleWhileRevalidate(request));
    }
});

self.addEventListener("message", (event) => {
    if (event.data === "SKIP_WAITING") self.skipWaiting();
});
`;

let current = null;
try {
    current = await readFile(outputFile, "utf8");
} catch (error) {
    if (error.code !== "ENOENT") throw error;
}

if (check) {
    if (current !== output) throw new Error("sw.js is stale. Run: node docs/tools/build-service-worker.mjs");
    console.log(`Verified Service Worker ${version} with ${routes.length} precached resources (${(precacheBytes / 1024).toFixed(1)} KiB).`);
} else {
    await writeFile(outputFile, output, "utf8");
    console.log(`Generated Service Worker ${version} with ${routes.length} precached resources (${(precacheBytes / 1024).toFixed(1)} KiB).`);
}
