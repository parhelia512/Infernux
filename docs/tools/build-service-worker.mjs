import { createHash } from "node:crypto";
import { readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const docsRoot = path.resolve("docs");
const outputFile = path.join(docsRoot, "sw.js");
const check = process.argv.includes("--check");
const maxPrecacheBytes = 768 * 1024;

async function namedRoutes(directory, pattern) {
    return (await readdir(path.join(docsRoot, directory), { withFileTypes: true }))
        .filter((entry) => entry.isFile() && pattern.test(entry.name))
        .map((entry) => `/${directory}/${entry.name}`);
}

const hashedTemplateStyles = await namedRoutes("css", /^wiki-template\.[a-f0-9]{16}\.css$/);
if (hashedTemplateStyles.length !== 1) throw new Error(`Expected one content-hashed Wiki template style, found ${hashedTemplateStyles.length}.`);
const shellPages = [
    "/offline.html",
    "/404.html",
    "/index.html",
    "/start.html",
    "/learn.html",
    "/roadmap.html",
    "/community.html",
    "/download.html",
];

async function shellRuntimeRoutes(pages) {
    const routes = new Set();
    for (const pageRoute of pages) {
        const source = await readFile(path.join(docsRoot, pageRoute.slice(1)), "utf8");
        for (const match of source.matchAll(/\b(?:href|src)=["']([^"']+\.(?:css|js)(?:[?#][^"']*)?)["']/gi)) {
            const resolved = new URL(match[1], `https://infernux-engine.com${pageRoute}`);
            if (resolved.origin !== "https://infernux-engine.com") continue;
            if (!/^\/(?:css|js)\//.test(resolved.pathname)) {
                throw new Error(`${pageRoute}: shell runtime '${match[1]}' must stay under /css or /js.`);
            }
            routes.add(resolved.pathname);
        }
    }
    return [...routes];
}

const precacheRoutes = [
    ...shellPages,
    "/site.webmanifest",
    "/assets/logo.png",
    ...await shellRuntimeRoutes(shellPages),
    ...await namedRoutes("assets/fonts", /\.woff2$/),
].filter((route, index, values) => values.indexOf(route) === index).sort();

// These files participate in the worker version without being fetched during
// installation. A data/runtime change can still announce a new site version,
// while the larger API index and route-specific generated-page assets remain
// on demand and are cached only after the visitor requests them. The compact
// generated API index remains on demand and is cached after it is requested.
const evidenceRoutes = [
    ...precacheRoutes,
    "/api-index.json",
    "/docs-manifest.json",
    "/release.json",
    "/release-notes.json",
    ...await namedRoutes("css", /\.css$/),
    ...await namedRoutes("js", /\.js$/),
    ...hashedTemplateStyles,
].filter((route, index, values) => values.indexOf(route) === index).sort();

const evidence = [];
let precacheBytes = 0;
for (const route of precacheRoutes) {
    const content = await readFile(path.join(docsRoot, route.slice(1)));
    precacheBytes += content.length;
}
for (const route of evidenceRoutes) {
    const content = await readFile(path.join(docsRoot, route.slice(1)));
    const canonicalContent = /\.(?:css|html|js|json|txt|webmanifest)$/i.test(route)
        ? Buffer.from(content.toString("utf8").replace(/\r\n/g, "\n"), "utf8")
        : content;
    evidence.push(`${route}\0${createHash("sha256").update(canonicalContent).digest("hex")}`);
}
if (precacheBytes > maxPrecacheBytes) {
    throw new Error(`PWA precache is ${(precacheBytes / 1024).toFixed(1)} KiB; budget is ${(maxPrecacheBytes / 1024).toFixed(1)} KiB.`);
}

const version = createHash("sha256").update(evidence.join("\n")).digest("hex").slice(0, 16);
const output = `/* Generated by docs/tools/build-service-worker.mjs. Do not edit directly. */
const CACHE_PREFIX = "infernux-pwa-";
const CACHE_VERSION = "${version}";
const CACHE_NAME = CACHE_PREFIX + CACHE_VERSION;
const RUNTIME_CACHE_NAME = CACHE_PREFIX + "runtime-v1";
const RUNTIME_MAX_ENTRIES = 96;
const OFFLINE_URL = "/offline.html";
const PRECACHE_URLS = ${JSON.stringify(precacheRoutes, null, 2)};
// Build provenance only: these inputs are hashed, never implicitly fetched.
const VERSION_INPUT_COUNT = ${evidenceRoutes.length};

function canCache(response) {
    return response && response.ok && (response.type === "basic" || response.type === "default");
}

function isPrecachedRequest(request) {
    const raw = typeof request === "string" ? request : request.url;
    return PRECACHE_URLS.includes(new URL(raw, self.location.origin).pathname);
}

async function matchManaged(request) {
    const options = { ignoreSearch: true };
    const core = await caches.open(CACHE_NAME);
    const coreMatch = await core.match(request, options);
    if (coreMatch) return coreMatch;
    const runtime = await caches.open(RUNTIME_CACHE_NAME);
    return runtime.match(request, options);
}

async function putRuntime(request, response) {
    if (!canCache(response) || isPrecachedRequest(request)) return;
    try {
        const cache = await caches.open(RUNTIME_CACHE_NAME);
        await cache.delete(request, { ignoreSearch: true });
        await cache.put(request, response.clone());
        const keys = await cache.keys();
        const overflow = keys.length - RUNTIME_MAX_ENTRIES;
        if (overflow > 0) {
            await Promise.all(keys.slice(0, overflow).map((key) => cache.delete(key)));
        }
    } catch (error) {
        // Storage pressure must never replace a successful network response
        // with an offline error. Runtime caching is a best-effort enhancement.
    }
}

async function networkFirst(request, navigationFallback = false) {
    try {
        const response = await fetch(request);
        await putRuntime(request, response);
        return response;
    } catch (error) {
        const cached = await matchManaged(request);
        if (cached) return cached;
        if (navigationFallback) {
            const pathname = new URL(request.url).pathname;
            if (pathname === "/" || pathname === "/index.html") {
                const home = await matchManaged("/index.html");
                if (home) return home;
            }
            return matchManaged(OFFLINE_URL);
        }
        throw error;
    }
}

async function cacheFirst(request) {
    const cached = await matchManaged(request);
    if (cached) return cached;
    const response = await fetch(request);
    await putRuntime(request, response);
    return response;
}

async function staleWhileRevalidate(request) {
    const cached = await matchManaged(request);
    const refresh = fetch(request).then(async (response) => {
        await putRuntime(request, response);
        return response;
    }).catch(() => null);
    if (cached) return cached;
    return await refresh || Response.error();
}

async function migrateLegacyCache(cacheName) {
    try {
        const legacy = await caches.open(cacheName);
        for (const request of await legacy.keys()) {
            if (isPrecachedRequest(request)) continue;
            const response = await legacy.match(request);
            if (response) await putRuntime(request, response);
        }
    } catch (error) {
        // A failed migration must not prevent the new core shell activating.
    }
}

async function pruneRuntimeCoreEntries() {
    const runtime = await caches.open(RUNTIME_CACHE_NAME);
    await Promise.all(PRECACHE_URLS.map((route) => runtime.delete(route, { ignoreSearch: true })));
}

async function activateManagedCaches() {
    const keys = await caches.keys();
    const obsolete = keys.filter((key) => key.startsWith(CACHE_PREFIX) && key !== CACHE_NAME && key !== RUNTIME_CACHE_NAME);
    for (const key of obsolete) await migrateLegacyCache(key);
    await Promise.all(obsolete.map((key) => caches.delete(key)));
    await pruneRuntimeCoreEntries();
    await self.clients.claim();
}

self.addEventListener("install", (event) => {
    event.waitUntil(caches.open(CACHE_NAME).then((cache) => cache.addAll(PRECACHE_URLS)));
});

self.addEventListener("activate", (event) => {
    event.waitUntil(activateManagedCaches());
});

self.addEventListener("fetch", (event) => {
    const request = event.request;
    if (request.method !== "GET") return;
    const url = new URL(request.url);
    if (url.origin !== self.location.origin || url.pathname === "/sw.js") return;

    if (request.mode === "navigate") {
        event.respondWith(networkFirst(request, true));
        return;
    }

    if (url.searchParams.has("v") && /\\.(?:css|js)$/i.test(url.pathname)) {
        event.respondWith(networkFirst(request));
        return;
    }

    if (/\\/css\\/wiki-template\\.[a-f0-9]{16}\\.css$/.test(url.pathname)
        || /\\/assets\\/fonts\\//.test(url.pathname)
        || /\\.(?:avif|gif|jpe?g|png|webp|woff2?)$/i.test(url.pathname)) {
        event.respondWith(cacheFirst(request));
        return;
    }

    if (/\\.(?:json|txt)$/i.test(url.pathname)) {
        event.respondWith(networkFirst(request));
        return;
    }

    if (/\\.(?:css|js|webmanifest)$/i.test(url.pathname)) {
        event.respondWith(staleWhileRevalidate(request));
    }
});

self.addEventListener("message", (event) => {
    if (event.data === "SKIP_WAITING") self.skipWaiting();
});
`;

let current = null;
try {
    current = await readFile(outputFile, "utf8");
} catch (error) {
    if (error.code !== "ENOENT") throw error;
}

if (check) {
    if (current !== output) throw new Error("sw.js is stale. Run: node docs/tools/build-service-worker.mjs");
    console.log(`Verified Service Worker ${version} with ${precacheRoutes.length} core shell resources (${(precacheBytes / 1024).toFixed(1)} KiB) and ${evidenceRoutes.length} version inputs.`);
} else {
    await writeFile(outputFile, output, "utf8");
    console.log(`Generated Service Worker ${version} with ${precacheRoutes.length} core shell resources (${(precacheBytes / 1024).toFixed(1)} KiB) and ${evidenceRoutes.length} version inputs.`);
}
